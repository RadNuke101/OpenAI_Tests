def return_last_three_numbers(input_str):
    return input_str[-3:]

# Prompt: return the last three numbers
# Input: ['938-242-504'] 
# Output: 504

# Test cases
print(return_last_three_numbers('938-242-504')) # 504
print(return_last_three_numbers('308-916-545')) # 545
print(return_last_three_numbers('623-599-749')) # 749
print(return_last_three_numbers('981-424-843')) # 843
print(return_last_three_numbers('118-980-214')) # 214
print(return_last_three_numbers('244-655-094')) # 094
print(return_last_three_numbers('830-941-991')) # 991
print(return_last_three_numbers('911-186-562')) # 562
print(return_last_three_numbers('002-500-200')) # 200
