def first_word(input_str):
    return input_str.split()[0]

# Prompt: return the first word of the inputted phrase
# Input: ['Nancy FreeHafer'] 
# Output: Nancy
print(first_word('Nancy FreeHafer')) 

# Input: ['Andrew Cencici'] 
# Output: Andrew
print(first_word('Andrew Cencici')) 

# Input: ['Jan Kotas'] 
# Output: Jan
print(first_word('Jan Kotas')) 

