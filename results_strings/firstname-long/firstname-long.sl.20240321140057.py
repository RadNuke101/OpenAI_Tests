def get_first_word(input_phrase):
    return input_phrase.split()[0]

# Prompt: return the first word of the inputted phrase
# Input: ['Nancy FreeHafer'] 
# Output: Nancy
print(get_first_word('Nancy FreeHafer')) 

# Input: ['Andrew Cencici'] 
# Output: Andrew
print(get_first_word('Andrew Cencici')) 

# Input: ['Jan Kotas'] 
# Output: Jan
print(get_first_word('Jan Kotas')) 

